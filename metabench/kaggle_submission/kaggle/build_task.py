#!/usr/bin/env python3
"""Build the single-file Kaggle task bundle."""

from __future__ import annotations

import base64
import json
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
KAGGLE_DIR = ROOT / "kaggle"
OUTPUT_PATH = KAGGLE_DIR / "task.py"
BUNDLE_DIRS = ("harness", "verifiers", "generators")
MIN_SIZE_BYTES = 50 * 1024
MAX_SIZE_BYTES = 500 * 1024

TASK_TEMPLATE = '''"""Generated by kaggle/build_task.py. DO NOT EDIT.

S4 must rename the evaluation_data column "class" -> "class_" before calling
`.evaluate(...)` because Python cannot bind a keyword argument named `class`.
"""

from __future__ import annotations

import base64
import json as _json
import sys
import tempfile
from pathlib import Path
from typing import Any

EMBEDDED_MODULES = __EMBEDDED_MODULES__


def _ensure_bundle() -> Path:
    bundle_dir = Path(tempfile.gettempdir()) / "metacog_v1_bundle"
    bundle_dir.mkdir(parents=True, exist_ok=True)
    for filename, encoded in EMBEDDED_MODULES.items():
        target = bundle_dir / filename
        target.parent.mkdir(parents=True, exist_ok=True)
        decoded = base64.b64decode(encoded).decode("utf-8") if encoded else ""
        if target.exists():
            existing = target.read_text(encoding="utf-8")
            if existing == decoded:
                continue
        target.write_text(decoded, encoding="utf-8")

    bundle_str = str(bundle_dir)
    if bundle_str not in sys.path:
        sys.path.insert(0, bundle_str)
    return bundle_dir


_BUNDLE_DIR = _ensure_bundle()

try:
    import kaggle_benchmarks as kbench
except Exception:  # pragma: no cover - local dry runs do not ship the SDK.
    class _KBenchStub:
        @staticmethod
        def task(*args: Any, **kwargs: Any):
            del args, kwargs

            def decorator(func):
                return func

            return decorator

    kbench = _KBenchStub()

from harness.runner import run_instance


def _is_missing(value: Any) -> bool:
    if value is None or type(value).__name__ == "NAType":
        return True
    try:
        return bool(value != value)
    except Exception:
        return False


def _coerce_json(value: Any, *, default: Any = None) -> Any:
    if _is_missing(value):
        return default
    if isinstance(value, (dict, list)):
        return value
    if isinstance(value, str):
        stripped = value.strip()
        if not stripped:
            return default
        return _json.loads(stripped)
    return value


def _normalize_components(value: Any) -> list[dict[str, Any]] | None:
    parsed = _coerce_json(value, default=None)
    if parsed is None:
        return None
    if not isinstance(parsed, list):
        raise TypeError("components must be a list, JSON string, or null")
    return parsed


@kbench.task(name="metacog_optim_v1")
def run(
    llm: Any,
    id: str,
    class_: str,
    difficulty: str,
    seed: int,
    instance: Any,
    gold_objective: float,
    baseline_objective: float,
    value_cap: float,
    wall_budget_s: int = 1800,
    components: Any = None,
) -> float:
    del id, wall_budget_s

    inst = _coerce_json(instance, default=None)
    if not isinstance(inst, dict):
        raise TypeError("instance must be a dict or JSON object string")

    row = run_instance(
        llm=llm,
        instance=inst,
        cls=str(class_),
        difficulty=str(difficulty),
        seed=int(seed),
        gold_objective=float(gold_objective),
        baseline_objective=float(baseline_objective),
        value_cap=float(value_cap),
        components=_normalize_components(components),
    )
    return float(row["score"])
'''


def _iter_bundle_sources() -> list[tuple[str, str]]:
    modules: list[tuple[str, str]] = []
    for subdir in BUNDLE_DIRS:
        root = ROOT / subdir
        if not root.exists():
            raise FileNotFoundError(f"missing bundle directory: {root}")
        for path in sorted(root.rglob("*.py")):
            if "__pycache__" in path.parts:
                continue
            source = path.read_text(encoding="utf-8")
            if path.name == "__init__.py" and not source.strip():
                continue
            relpath = path.relative_to(ROOT).as_posix()
            modules.append((relpath, source))
    return modules


def build_task_source() -> tuple[str, int]:
    modules = {
        relpath: base64.b64encode(source.encode("utf-8")).decode("ascii")
        for relpath, source in _iter_bundle_sources()
    }
    rendered_modules = json.dumps(modules, indent=4, sort_keys=True)
    return TASK_TEMPLATE.replace("__EMBEDDED_MODULES__", rendered_modules), len(modules)


def main() -> None:
    KAGGLE_DIR.mkdir(parents=True, exist_ok=True)
    task_source, n_modules = build_task_source()
    OUTPUT_PATH.write_text(task_source, encoding="utf-8")

    size_bytes = OUTPUT_PATH.stat().st_size
    if not (MIN_SIZE_BYTES < size_bytes < MAX_SIZE_BYTES):
        raise RuntimeError(
            f"task.py size {size_bytes} bytes outside expected range "
            f"({MIN_SIZE_BYTES}, {MAX_SIZE_BYTES})"
        )

    size_kb = size_bytes / 1024.0
    print(f"build_task: wrote kaggle/task.py, {size_kb:.1f} KB, {n_modules} modules.")


if __name__ == "__main__":
    main()
