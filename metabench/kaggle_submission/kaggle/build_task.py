#!/usr/bin/env python3
"""Build the single-file Kaggle task bundle by concatenating sources directly.

Readable flat merge — no base64, no EMBEDDED_MODULES. Inlines harness + verifiers
in dependency order; strips cross-module imports; renames each verifier's verify()
to {cls}_verify() to avoid collisions; builds CLASS_TO_VERIFIER explicitly.
Generators are NOT included (instances arrive via evaluation_data, not generated).

DO NOT add `from __future__ import annotations` to the TASK_HEADER or any inlined
module — kbench's Score type inference fails on stringified `-> float` return
annotations (PEP 563 makes all annotations strings, breaking _infer_result_type).
"""

from __future__ import annotations

import re
import textwrap
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
KAGGLE_DIR = ROOT / "kaggle"
OUTPUT_PATH = KAGGLE_DIR / "task.py"

KNOWN_CLASSES = ("cjs", "graphcol", "mwis", "steiner", "tsp", "ve")

# Merge order: each (subdir, stem) pair, verifiers get verify() renamed.
# harness/runner must come last among harness files.
BUNDLE_ORDER = [
    ("harness", "protocol"),
    ("harness", "render_nl"),
    ("harness", "scoring"),
    ("verifiers", "cjs"),
    ("verifiers", "graphcol"),
    ("verifiers", "mwis"),
    ("verifiers", "steiner"),
    ("verifiers", "tsp"),
    ("verifiers", "ve"),
    ("harness", "prompt"),
    ("harness", "runner"),
]

# Strip internal imports including multi-line `from X import (\n...\n)` forms.
_FUTURE_IMPORT = re.compile(
    r"^\s*from\s+__future__\s+import\s+annotations\s*\n",
    re.MULTILINE,
)
# Matches single-line: `from harness.x import y` or `import harness.x`
_INTERNAL_IMPORT_SINGLE = re.compile(
    r"^[ \t]*(?:from|import)\s+(?:harness|generators|verifiers)\b[^\n]*\n",
    re.MULTILINE,
)
# Matches multi-line: `from harness.x import (\n    a,\n    b,\n)`
_INTERNAL_IMPORT_MULTI = re.compile(
    r"^[ \t]*from\s+(?:harness|generators|verifiers)\b[^\n]*\(\s*\n(?:[^\)]*\n)*?\s*\)\s*\n",
    re.MULTILINE,
)

TASK_HEADER = '''\
"""Generated by kaggle/build_task.py. DO NOT EDIT."""
# DO NOT add `from __future__ import annotations` — kbench type inference
# fails on stringified `-> float` annotations (PEP 563 breaks _infer_result_type).

import inspect
import json as _json
import re as _re
from typing import Any

try:
    import kaggle_benchmarks as kbench
except Exception:  # pragma: no cover - local dry runs do not ship the SDK.
    class _KBenchStub:
        @staticmethod
        def task(*args: Any, **kwargs: Any):
            del args, kwargs

            def decorator(func):
                return func

            return decorator

    kbench = _KBenchStub()

'''

VERIFIER_REGISTRY = '''\
# ── Verifier registry (built from inlined verify functions above) ─────────────
_SCHEMA_RE = _re.compile(r"BEST_GUESS(?: JSON)? schema:\\s*(.*)", _re.IGNORECASE | _re.DOTALL)

CLASS_TO_VERIFIER: dict[str, Any] = {
    "cjs":      cjs_verify,
    "graphcol": graphcol_verify,
    "mwis":     mwis_verify,
    "steiner":  steiner_verify,
    "tsp":      tsp_verify,
    "ve":       ve_verify,
}

CLASS_TO_BEST_GUESS_SCHEMA: dict[str, str] = {}
for _cls, _fn in CLASS_TO_VERIFIER.items():
    _doc = inspect.getdoc(_fn) or ""
    _m = _SCHEMA_RE.search(_doc)
    if _m:
        _schema = _m.group(1).strip()
        if _schema:
            CLASS_TO_BEST_GUESS_SCHEMA[_cls] = _schema

'''

TASK_RUN = '''\

def _is_missing(value: Any) -> bool:
    if value is None or type(value).__name__ == "NAType":
        return True
    try:
        return bool(value != value)
    except Exception:
        return False


def _coerce_json(value: Any, *, default: Any = None) -> Any:
    if _is_missing(value):
        return default
    if isinstance(value, (dict, list)):
        return value
    if isinstance(value, str):
        stripped = value.strip()
        if not stripped:
            return default
        return _json.loads(stripped)
    return value


def _normalize_components(value: Any) -> list[dict[str, Any]] | None:
    parsed = _coerce_json(value, default=None)
    if parsed is None:
        return None
    if not isinstance(parsed, list):
        raise TypeError("components must be a list, JSON string, or null")
    return parsed


@kbench.task(name="meta_hch_bench")
def run(
    llm: Any,
    id: str,
    class_: str,
    difficulty: str,
    seed: int,
    instance: Any,
    gold_objective: float,
    baseline_objective: float,
    value_cap: float,
    wall_budget_s: int = 1800,
    components: Any = None,
) -> float:
    del id, wall_budget_s

    inst = _coerce_json(instance, default=None)
    if not isinstance(inst, dict):
        raise TypeError("instance must be a dict or JSON object string")

    row = run_instance(
        llm=llm,
        instance=inst,
        cls=str(class_),
        difficulty=str(difficulty),
        seed=int(seed),
        gold_objective=float(gold_objective),
        baseline_objective=float(baseline_objective),
        value_cap=float(value_cap),
        components=_normalize_components(components),
    )
    return float(row["score"])
'''


def _clean(src: str, *, rename_verify: str | None = None) -> str:
    src = _FUTURE_IMPORT.sub("", src)
    src = _INTERNAL_IMPORT_MULTI.sub("", src)   # multi-line first
    src = _INTERNAL_IMPORT_SINGLE.sub("", src)  # then single-line
    # Fix empty try blocks left by stripped imports: `try:\n[blanks]    except`.
    src = re.sub(
        r"([ \t]*)try:\n((?:[ \t]*\n)*)([ \t]+except\b)",
        lambda m: f"{m.group(1)}try:\n{m.group(2)}{m.group(1)}    pass  # import inlined\n{m.group(3)}",
        src,
    )
    if rename_verify:
        # Rename public verify() only; leave private _*() helpers as-is.
        src = re.sub(r"\bdef verify\b", f"def {rename_verify}_verify", src)
        # Update internal calls to verify( within same file.
        src = re.sub(r"\bverify\(", f"{rename_verify}_verify(", src)
    # Collapse runs of blank lines > 2 down to 2.
    src = re.sub(r"\n{3,}", "\n\n", src)
    return src.strip()


def build_task_source() -> str:
    parts: list[str] = [TASK_HEADER]

    for subdir, stem in BUNDLE_ORDER:
        path = ROOT / subdir / f"{stem}.py"
        if not path.exists():
            raise FileNotFoundError(f"missing: {path}")
        src = path.read_text(encoding="utf-8")
        rename = stem if subdir == "verifiers" else None
        cleaned = _clean(src, rename_verify=rename)
        parts.append(f"# {'─' * 70}\n# {subdir}/{stem}.py\n# {'─' * 70}\n\n{cleaned}\n\n")

        if subdir == "verifiers" and stem == "ve":
            parts.append(VERIFIER_REGISTRY)

    parts.append(TASK_RUN)
    return "".join(parts)


def main() -> None:
    KAGGLE_DIR.mkdir(parents=True, exist_ok=True)
    task_source = build_task_source()
    OUTPUT_PATH.write_text(task_source, encoding="utf-8")

    size_bytes = OUTPUT_PATH.stat().st_size
    size_kb = size_bytes / 1024.0
    n_modules = len(BUNDLE_ORDER)
    print(f"build_task: wrote kaggle/task.py, {size_kb:.1f} KB, {n_modules} modules.")
    if size_kb > 500:
        print(f"WARNING: task.py is {size_kb:.0f} KB — unusually large for a flat merge.")


if __name__ == "__main__":
    main()
